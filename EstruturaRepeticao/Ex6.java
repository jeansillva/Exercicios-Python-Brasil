package EstruturaRepeticao;

public class Ex6 {
    public static void main(String[] args) {
        System.out.println("Numero abaixo");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        System.out.println("Numero do lado:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}
